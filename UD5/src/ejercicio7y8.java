
public class ejercicio7y8 {

	public static void main(String[] args) {

		int num=1;
		
		while (num<=100) {
			System.out.println(num);
			num++;
		}
		System.out.println("Hasta aquí es mediante el bucle While y a continuación mediante el bucle For");
		
		for (int digit=1; digit < 101; ++digit) {
			System.out.println(digit);
		}
	}
}
